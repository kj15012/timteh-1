    <h1>Batman v Superman: Dawn of Justice</h1>
    <h2>About the film</h2>
    <p>From director Zack Snyder comes “Batman v Superman: Dawn of Justice,” starring Oscar winner Ben Affleck (“Argo”)
        as Batman/Bruce Wayne and Henry Cavill as Superman/Clark Kent in the characters’ first big-screen pairing.</p>
    <p>Fearing the actions of&nbsp;a god-like Super Hero&nbsp;left unchecked, Gotham City’s own formidable, forceful
        vigilante takes on Metropolis’s most revered, modern-day savior, while the world wrestles with what sort of hero
        it really needs.&nbsp; And with Batman and Superman at war with each other, a new threat quickly arises, putting
        mankind in greater danger than it has ever known before. </p>
    <p>Directed by Zack Snyder, the film also stars Oscar nominees Amy Adams (“American Hustle”) as Lois Lane, Jesse
        Eisenberg (“The Social Network”) as Lex Luthor, Diane Lane (“Unfaithful”) as Martha Kent, and Laurence Fishburne
        (“What’s Love Got to Do with It”) as Perry White; Oscar winners Jeremy Irons (“Reversal of Fortune”) as Alfred,
        and Holly Hunter (“The Piano”) as Senator Finch; and Gal Gadot as Wonder Woman/Diana Prince.</p>
    <p>Snyder directed from a screenplay written by Chris Terrio and David S. Goyer, based on characters from DC Comics,
        including Batman, created by Bob Kane with Bill Finger, and Superman, created by Jerry Siegel and Joe Shuster.
        The film is produced by Charles Roven and Deborah Snyder, with Wesley Coller, Geoff Johns and David S. Goyer
        serving as executive producers.</p>
    <p>Snyder’s behind-the-scenes creative team includes director of photography Larry Fong (“300,” “Watchmen”) and production
        designer Patrick Tatopoulos (“300: Rise of an Empire”), and from his “Man of Steel” team, editor David Brenner,
        costume designer Michael Wilkinson, visual effects supervisor John “DJ” DesJardin, and Oscar-winning composer
        Hans Zimmer (“The Lion King”) working in conjunction with composer Junkie XL (“Mad Max: Fury Road”).</p>
    <p>Warner Bros. Pictures presents, an Atlas Entertainment/Cruel and Unusual production, a Zack Snyder film,
        “Batman v Superman: Dawn of Justice.”  The film opens nationwide in IMAX, Dolby Cinema, RealD 3D and in 2D,
        and will be distributed worldwide by Warner Bros. Pictures, a Warner Bros. Entertainment Company.</p>
    <h2>Plot</h2>
    <p>Eighteen months after the destructive battle with General Zod in Metropolis (Man of Steel), Superman has become
        a controversial figure. Daily Planet journalist Clark Kent, Superman's alter ego, has moved in with Lois Lane.
        Billionaire Bruce Wayne, who has operated in Gotham City as Batman for nearly two decades, sees Superman as
        a potential threat to humanity. After learning of Batman's activities, Superman also views him as a threat,
        and seeks to stop him via the Daily Planet articles under his disguise.</p>
    <p>Wayne learns that weapon trafficker Anatoli Knyazev has been contacting LexCorp's mogul Lex Luthor. Meanwhile,
        Luthor tries to convince Senator June Finch to allow him to import kryptonite retrieved from the Indian Ocean
        (results of Zod's terraforming attempt), claiming to use it as a "deterrent" against Kryptonians. He also makes
        side dealings with Finch's subordinate and demands access to Zod's body and the Kryptonian scout ship.
        In the next meeting with Luthor, Finch denies his request.</p>
    <p>Wayne attends Luthor's party at LexCorp, where he meets mysterious antiques dealer Diana Prince, and retrieves
        data from the company's mainframe. The data drive, however, is stolen by Diana, who later returns it to Bruce,
        due to her inability to decrypt the data. While decrypting the drive at the Batcave, Wayne receives a vision
        of a post-apocalyptic world, where he leads a group of rebels against Superman. He is snapped out of the vision
        by a mysterious time traveler, who warns him of Lois Lane's crucial role in the distant future, and urges him
        to find "the others". Wayne later realizes that Lex is not only experimenting with kryptonite, but also
        investigating metahumans. One of them is Diana herself, who is an immortal warrior. Wayne admits to Alfred
        Pennyworth that he plans to steal the Kryptonite to weaponize it, should it become necessary to fight Superman.
        Batman pursues the convoy carrying the kryptonite from the White Portuguese ship to LexCorp, but Superman intercedes
        and orders him to cease his activities.</p>
    <h2>Release</h2>
    <p>Batman v Superman: Dawn of Justice premiered at Auditorio Nacional in Mexico City on March 19, 2016,
        followed by a New York City premiere on March 20 at Radio City Music Hall. Following the Brussels attacks,
        Warner Bros. originally cancelled the red carpet of the London premiere, but decided to carry on with
        the premiere for the fans. The film was released in the United States and the United Kingdom on March 25 in 3D.
        It opened simultaneously in North America, China and Japan, the world's three largest film markets, as well as
        additional international territories, with the exception of Poland, where theaters do not open on Good Friday.
        The film debuted simultaneously across 30,000 screens in nearly every major foreign territory across 61 markets,
        including China, with domestic open across roughly 4,242 locations of which 3,500 theaters (85%) were in 3D,
        390 IMAX screens, 470 PLF locations, 150 D-Box theaters and ten 70 mm prints.</p>

